public function pesquisarMaxPKAtivoIdusuariopromotorPorStatus($idUsuarioPromotor,$status)
{
    $daofactory = NULL;
    $retorno = NULL;
    try {
        $daofactory = DAOFactory::getDAOFactory();
        $daofactory->open();
        $daofactory->beginTransaction();
        
       $bo = new RegistroIndicacaoBusinessImpl();
       $retorno = $bo->pesquisarMaxPKAtivoIdusuariopromotorPorStatus($daofactory, $idUsuarioPromotor,$status);
       if ($retorno->msgcode == ConstantesMensagem::COMANDO_REALIZADO_COM_SUCESSO) {
            $daofactory->commit();
        } else {
            $daofactory->rollback();
        }
        
    } catch (Exception $e) {
        // rollback na transação
        $daofactory->rollback();
    } finally {
        try {
            $daofactory->close();
        } catch (Exception $e) {
            // faz algo
        }
    }

    return $retorno;
}

