*
* PesquisarMaxPKAtivoIdPorStatus() - Usado para invocar a classe de negócio VersaoBusinessImpl de forma geral
* a buscar a MAIOR PK pra um dado status.
*
* @param status
* @return VersaoDTO
*
*/

public function PesquisarMaxPKAtivoVersaoPorStatus($versao,$status)
{
    $daofactory = NULL;
    $retorno = NULL;
    try {
        $daofactory = DAOFactory::getDAOFactory();
        $daofactory->open();
        $daofactory->beginTransaction();
        
       $bo = new VersaoBusinessImpl();
       $retorno = $bo->PesquisarMaxPKAtivoVersaoPorStatus($daofactory, $versao,$status);
       if ($retorno->msgcode == ConstantesMensagem::COMANDO_REALIZADO_COM_SUCESSO) {
            $daofactory->commit();
        } else {
            $daofactory->rollback();
        }
        
    } catch (Exception $e) {
        // rollback na transação
        $daofactory->rollback();
    } finally {
        try {
            $daofactory->close();
        } catch (Exception $e) {
            // faz algo
        }
    }

    return $retorno;
}

